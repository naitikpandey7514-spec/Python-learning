#Conditional Statement
#A conditional statement is used to make decisions in a program based on a condition.

#types

#if – Executes code if the condition is True.

#if-else – Executes one block if True, otherwise another block.

#if-elif-else – Checks multiple conditions.

#syntax
'''if condition:
    # code
elif condition:
    # code
else:
    # code'''


#eg
marks = 65

if marks >= 40:
    print("Result: Pass")
    print("Congratulations!")
else:
    print("Result: Fail")
    print("Better luck next time!")



